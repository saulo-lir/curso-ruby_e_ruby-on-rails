=begin

1) Módulos Ruby são similares a classes em relação ao fato de que também 
armazenam uma coleção de métodos, constantes , outras definições de módulos
e classes.

2) Diferente das classes, você não pode criar objetos baseados em módulos 
nem pode criar módulos que herdam desse módulo.

3) Módulos são um bom lugar para armazenar constantes em um local centralizado.

4) OBJETIVOS: 
- Primeiro eles agem como namespace, permitindo que você defina métodos cujos 
nomes não irão colidir com aqueles definidos em outras partes de um programa.

- Em segundo lugar, permitem que você compartilhe funcionalidade entre classes.

=end

