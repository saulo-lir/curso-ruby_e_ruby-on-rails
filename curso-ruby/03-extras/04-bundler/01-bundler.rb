=begin

1) O Bundler serve para gerenciar e informar quais são as gems que são 
necessárias para o projeto que está sendo feito

2) O Bundler também é uma gem, então para utilizá-lo, é necessário instala-lo também
    gem install bundler

3) site: bundler.io

4) O arquivo Gemfile que deve ser criado na pasta do projeto, é onde deve ser inseridas
as dependências do projeto

5) Após inseridas as gems no arquivo Gemfile, devemos rodar o comando bundle ou bundle install 
dentro da pasta onde foi criado este arquivo, e aguardar que as gems informadas sejam 
instaladas caso já não existam no computador

=end

